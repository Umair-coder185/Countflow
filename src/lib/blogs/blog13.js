<p>Research on inner speech doesn't fully support that idea. Inner speech plays a real role in word recognition and comprehension, and attempts to suppress it tend to hurt understanding once the material gets harder, according to a <a href="https://www.centerforinquiry.org/review-of-speed-reading-studies/" target="_blank">Center for Inquiry review </a> of speed-reading studies . Readers who push speed this way often answer the easy, big-picture questions fine. They struggle on anything that needs detail.</p>
